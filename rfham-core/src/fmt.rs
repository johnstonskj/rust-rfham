pub trait Formatter {
    fn fmt(&self) -> String {
        self.fmt_with(&FormatterOptions::default())
    }

    fn fmt_with(&self, options: &FormatterOptions) -> String;
}

#[derive(Clone, Copy, Debug, Default, PartialEq)]
pub struct FormatterOptions {
    precision: Option<usize>,
}

